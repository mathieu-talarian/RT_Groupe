#include "my_mlx.h"

void		rgb_s_mult(t_rgb *r, double a)
{
	r->r *= a;
	r->g *= a;
	r->b *= a;
}

void		rgb_s_div(t_rgb *r, double a)
{
	r->r /= a;
	r->g /= a;
	r->b /= a;
}
