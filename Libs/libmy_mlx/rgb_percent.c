#include "my_mlx.h"

void		rgb_to_percent(t_rgb *r)
{
	r->r /= 255;
	r->r *= 100;
	r->g /= 255;
	r->g *= 100;
	r->b /= 255;
	r->b *= 100;
}

void		percent_to_rgb(t_rgb *r)
{
	r->r /= 100;
	r->r *= 255;
	r->g /= 100;
	r->g *= 255;
	r->b /= 100;
	r->b *= 255;
}
