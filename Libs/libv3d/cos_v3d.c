#include "libv3d.h"

double	cos_v3d(t_v3d v1, t_v3d v2)
{
	double ret;

	v1 = unit_v3d(v1);
	v2 = unit_v3d(v2);
	ret = v1.x * v2.x + v1.y * v2.y + v1.z * v2.z;
	return (ret);
}
