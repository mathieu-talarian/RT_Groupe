#include "libv3d.h"

void	print_v3d(t_v3d v, char *name)
{
	if (name)
		printf("%s(%f, %f, %f)", name, v.x, v.y, v.z);
	else
		printf("(%f, %f, %f)", v.x, v.y, v.z);
	printf("\n");
}
