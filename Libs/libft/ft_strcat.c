#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	char	*ps1;

	ps1 = s1;
	while (*ps1)
		ps1++;
	while (*s2)
	{
		*ps1 = *s2;
		ps1++;
		s2++;
	}
	*ps1 = '\0';
	return (s1);
}
